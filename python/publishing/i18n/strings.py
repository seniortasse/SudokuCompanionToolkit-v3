def normalize_language(language: str | None) -> str:
    raw = str(language or _DEFAULT_LANGUAGE).strip().lower()

    aliases = {
        "english": "en",
        "en-us": "en",
        "en-gb": "en",
        "french": "fr",
        "français": "fr",
        "francais": "fr",
        "german": "de",
        "deutsch": "de",
        "spanish": "es",
        "español": "es",
        "espanol": "es",
        "italian": "it",
        "italiano": "it",
        "portuguese": "pt",
        "português": "pt",
        "portugues": "pt",
        "dutch": "nl",
        "nederlands": "nl",
    }

    key = aliases.get(raw, raw)
    if key not in _STRINGS:
        return _DEFAULT_LANGUAGE
    return key


def tr(key: str, language: str | None = None) -> str:
    lang = normalize_language(language)
    if key in _STRINGS[lang]:
        return _STRINGS[lang][key]
    return _STRINGS[_DEFAULT_LANGUAGE].get(key, key)


def translate_difficulty_label(label: str | None, language: str | None = None) -> str:
    raw = str(label or "").strip().lower()
    mapping = {
        "easy": "difficulty_easy",
        "medium": "difficulty_medium",
        "hard": "difficulty_hard",
        "expert": "difficulty_expert",
    }
    if raw in mapping:
        return tr(mapping[raw], language)
    return str(label or "").strip()


def format_of_total(index: int, total: int, language: str | None = None) -> str:
    template = tr("of_total", language)
    return template.format(index=int(index), total=int(total))